package com.youdesign.online.domain;

public class StandardSize {
	private String size;
	private String subSize;
	private int bust;
	private int hip;
	private int waist;
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getSubSize() {
		return subSize;
	}
	public void setSubSize(String subSize) {
		this.subSize = subSize;
	}
	public int getBust() {
		return bust;
	}
	public void setBust(int bust) {
		this.bust = bust;
	}
	public int getHip() {
		return hip;
	}
	public void setHip(int hip) {
		this.hip = hip;
	}
	public int getWaist() {
		return waist;
	}
	public void setWaist(int waist) {
		this.waist = waist;
	}
	

}
